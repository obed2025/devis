	<title>Devis - About</title>
</svelte:head>

<p>
	<span class="primary">Devis</span> is a web-based tool designed to simplify the process of
	creating <span class="accent">price estimations</span>, especially for those working in
	<span class="success">construction</span> or similar fields. It provides a user-friendly experience
	accessible from any device, removing the need for error-prone and technical methods like paper, Excel,
	or Google Sheets.
</p>
<p>
	The platform is <span class="success">completely free</span> and aims to break down the barriers
	of complex software by offering an <span class="primary">intuitive interface</span>. Users can
	easily create, view, edit, and delete multiple estimations, export them as
	<span class="accent">PDF files</span>, and securely save their data in the browser, making
	estimation management straightforward and efficient.
</p>
<p>
	The name "<span class="primary">Devis</span>" comes from the
	<span class="accent">French word</span>
	for "estimate" or "quotation," which is commonly used in <span class="success">construction</span>
	and project management. This reflects the tool's purpose: to help users quickly and accurately generate
	professional estimates for their projects.
</p>

